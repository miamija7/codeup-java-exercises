public class StringExercise {
    public static void main(String[] args) {
        String  str1 = "We don't need no education",
                str2 = "We don't need no thought control",
                str3 = "Check \"this\" out, \"s inside of \"s!",
                str4 = "In windows, the main drive is usually C:\\",
                str5 = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.format("%s%n%s%n%s%n%s%n%s%n", str1, str2, str3, str4, str5);
    }
}
